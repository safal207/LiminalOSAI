; Пока что содержит заглушку, имитирующую передачу управления ядру.

section .text
    global _start

_start:
    ; В реальной системе здесь будет подготовка памяти и переход
    ; в защищённый режим. Пока просто завершаемся.
    mov eax, 60        ; номер системного вызова exit
    xor edi, edi       ; код возврата 0
    syscall
